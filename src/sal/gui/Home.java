    public static int countItemsInventory(TableModel model){
        int rowCount = model.getRowCount();
        int qtyTot = 0;
        
        for(int i=0;i<rowCount;i++){
            qtyTot += Integer.parseInt(model.getValueAt(i, 3).toString());
        }
        return qtyTot;
    }
    
