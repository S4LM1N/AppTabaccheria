    private void searchBarInventarioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchBarInventarioKeyTyped
        //Ricerca valori
        String text = searchBarInventario.getText();
            if(text.length() == 0) {
               sorter.setRowFilter(null);
            } else {
               try {
                  sorter.setRowFilter(RowFilter.regexFilter("(?i)" + text)); //(?i) permette di cercare tutte le parole tralasciando il maiuscolo o minuscolo
               } catch(PatternSyntaxException pse) {
                     System.out.println("Bad regex pattern");
               }
             }
    }//GEN-LAST:event_searchBarInventarioKeyTyped

