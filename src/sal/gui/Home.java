    private void btn_delMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_delMousePressed
        int selectedRow = tab_inv.getSelectedRow();
        
        if(selectedRow!=-1){
            if(tab_inv.getRowSorter()!=null){
                DefaultTableModel model = (DefaultTableModel) tab_inv.getModel();
                selectedRow = tab_inv.getRowSorter().convertRowIndexToModel(selectedRow);
                model.removeRow(selectedRow);
                rP1_inv.setText(String.valueOf(countItemsInventory(model)));
            } else{
                DefaultTableModel model = (DefaultTableModel) tab_inv.getModel();
                model.removeRow(selectedRow);
                rP1_inv.setText(String.valueOf(countItemsInventory(model)));
                try {
                    fileUtils.salvaInventario((DefaultTableModel) tab_inv.getModel(), "./inventario.txt");
                } catch (IOException ex) {
                    Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        else{
            errorMsg err = new errorMsg(null,true);
            err.setLocationRelativeTo(this);
            err.setMessage("Seleziona un dato nella tabella e poi premi sul pulasante RIMUOVI se vuoi eliminarlo!");
            err.setVisible(true);
        }
    }//GEN-LAST:event_btn_delMousePressed

