    private void btn_addToSellActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addToSellActionPerformed
        DefaultTableModel vendite = (DefaultTableModel) jTable1.getModel();
        float tot = 0;
        
        if(vendite.getRowCount()<=0){
            errorMsg err = new errorMsg(null, true);
            err.setLocationRelativeTo(this);
            err.setMessage("Non puo essere effettuata l'operazione se la tabella vendita e' vuota");
            err.setVisible(true);
        }
        else{
            ArrayList<Prodotto> prodottiVen = new ArrayList<>();
            for(int i=0;i<vendite.getRowCount();i++){
                String codice = vendite.getValueAt(i, 1).toString();
                String nomeProdotto = vendite.getValueAt(i, 0).toString();
                float prezzo = Float.parseFloat(vendite.getValueAt(i, 2).toString());
                int quantita = Integer.parseInt(vendite.getValueAt(i, 3).toString());
                tot += prezzo*quantita;
         
                
                prodottiVen.add(new Prodotto(codice,nomeProdotto,prezzo,quantita));
            }
            LocalDateTime data = LocalDateTime.now();
            data = data.truncatedTo(ChronoUnit.MINUTES);
            ven.addRow(new Object[]{ven.getRowCount()+1,tot,prodottiVen,data});
            try {
                fileUtils.salvaVendite(ven, "./vendite.txt");
            } catch (IOException ex) {
                Logger.getLogger(sellingMode.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.dispose();
        }
    }//GEN-LAST:event_btn_addToSellActionPerformed

