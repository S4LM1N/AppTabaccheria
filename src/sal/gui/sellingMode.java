    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        //Per evitare errori durante lettura codici
        try {
            Thread.sleep(300);
        } catch (InterruptedException ex) {
            Logger.getLogger(sellingMode.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        String codice = jTextField1.getText().toLowerCase();
        DefaultTableModel vendite = (DefaultTableModel)jTable1.getModel();
        jTextField1.setText("");
        boolean found = false;
        
        
        for(int i=0; i<inv.getRowCount(); i++){
            if(codice.toLowerCase().equals(inv.getValueAt(i, 1).toString().toLowerCase())){
                if(vendite.getRowCount() > 0){
                    for(int j=0; j<vendite.getRowCount();j++){
                        if(vendite.getValueAt(j, 1).toString().equals(codice)){
                            int qty = Integer.parseInt(vendite.getValueAt(j, 3).toString()) + 1;
                            float prezzo = Float.parseFloat(vendite.getValueAt(j, 2).toString());
                            vendite.setValueAt(qty, j, 3);
                            vendite.setValueAt(qty*prezzo  , j, 4);
                            updateTotaleVendita(vendite);
                            return;
                        }
                    }
                }
                
                Object nome = inv.getValueAt(i, 0);
                Object prezzo = inv.getValueAt(i, 2);
                vendite.addRow(new Object[]{nome,codice,prezzo,1,prezzo});
                updateTotaleVendita(vendite);
                return;
            }
        }
        
        if(found==false){
            
            errorMsg err = new errorMsg(null,true);
            err.setLocationRelativeTo(this);
            err.setMessage("Prodotto non trovato in inventario");
            err.setVisible(true);
            
            
            addInventory agg = new addInventory(null, true,inv,label);
            agg.setLocationRelativeTo(this);
            agg.setVisible(true);
        }
        
    }//GEN-LAST:event_jTextField1ActionPerformed

