    private void updateTotaleVendita(DefaultTableModel vendite){
        float totale = 0;
        if(vendite.getRowCount()>0){
            for(int i=0; i<vendite.getRowCount();i++)
            {
                totale += Float.parseFloat(vendite.getValueAt(i, 4).toString());
            }
            lbl_tot.setText(Float.toString(totale));
        }
    }

